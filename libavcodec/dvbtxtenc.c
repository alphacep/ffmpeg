uint8_t ucs2teletext1(uint8_t chr);

uint8_t ucs2teletext1(uint8_t chr)
{
    if (PARITY_81[chr] == 0) {
        chr = chr | 0x80;
    }
    return chr;
}

static int dvbtxt_encode_frame(AVCodecContext *avctx,
                             unsigned char *buf, int bufsize, const AVSubtitle *sub)
{
    DvbTxtContext *s = avctx->priv_data;
    ASSDialog *dialog;
    int i, j, start;

    for (j = 0; j < sub->num_rects; j++) {
        const char *ass = sub->rects[j]->ass;

        if (sub->rects[j]->type != SUBTITLE_ASS) {
            av_log(avctx, AV_LOG_ERROR, "Only SUBTITLE_ASS type supported.\n");
            return AVERROR(EINVAL);
        }

        dialog = ff_ass_split_dialog(s->ass_ctx, ass);
        if (!dialog)
            return AVERROR(ENOMEM);
//      printf ("Encoding rec %d text %s\n", j, dialog->text);

        buf[0] = 0x10; // EBU data

// ---------------------------------------------

#if 1
        start = 1;
        for (i = start; i < start + 46; i++) {
            buf[i] = 0x4;
        }
        buf[start] = 0x03; // EBU teletext
        buf[start + 1] = 0x2c;
        buf[start + 2] = 0xd5;
        buf[start + 3] = 0xe4;
        buf[start + 4] = 0xa8;
        buf[start + 5] = 0xa8;
        buf[start + 6] = 0xbf;
        buf[start + 7] = 0xbf;
        buf[start + 8] = 0x57;
        buf[start + 9] = 0xf4;
        buf[start + 10] = 0x57;
        buf[start + 11] = 0x7a;
        buf[start + 12] = 0x31;
        buf[start + 13] = 0xa8;
#endif

#if 1
    /*    d5 e4 a8 a8 40 a8 a8 a8 a8 b f4 a8 4   */
        start = 47;
        for (i = start; i < start + 46; i++) {
            buf[i] = 0x4;
        }
        buf[start] = 0x03; // EBU teletext
        buf[start + 1] = 0x2c;
        buf[start + 2] = 0xd5;
        buf[start + 3] = 0xe4;
        buf[start + 4] = 0xa8;
        buf[start + 5] = 0xa8;
        buf[start + 6] = 0x40;
        buf[start + 7] = 0xa8;
        buf[start + 8] = 0xa8;
        buf[start + 9] = 0xa8;
        buf[start + 10] = 0xa8;
        buf[start + 11] = 0xb;
        buf[start + 12] = 0xf4;
        buf[start + 13] = 0xa8;
#endif

// ---------------------------------------------

        start = 93;

        buf[start] = 0x03; // EBU teletext
        buf[start + 1] = 0x2c;

        /* f5 e4 a8 31 b0 e0 d0 d0 */

        buf[start + 2] = 0xf5; // 8 bytes with address
        buf[start + 3] = 0xe4;
        buf[start + 4] = 0xa8;
        buf[start + 5] = 0x31;
        buf[start + 6] = 0xb0;
        buf[start + 7] = 0xe0;
        buf[start + 8] = 0xd0;
        buf[start + 9] = 0xd0;

        uint8_t text[36];
        memset(text, 0x20, 36);
        for (i = 0; i < 36 && dialog->text[i] != 0 && (uint8_t)dialog->text[i] != 0xbd; i++) {
            text[i] = dialog->text[i];
        }
        for (i = 0; i < 36; i++) {
            buf[start + 10 + i] = ucs2teletext1(text[i]);
        }
        for (i = 0; i < 36; i++) {
            buf[start + 10 + i] = REVERSE_81[buf[start + 10 + i]];
        }

// ---------------------------------------------

        ff_ass_free_dialog(&dialog);
    }


    return 139;
}

static int dvbtxt_encode_close(AVCodecContext *avctx)
{
    DvbTxtContext *s = avctx->priv_data;
    ff_ass_split_free(s->ass_ctx);
    return 0;
}

const FFCodec ff_teletext_encoder = {
    .p.name         = "dvbtxt",
    CODEC_LONG_NAME("DVB Teletext Encoder"),
    .p.type         = AVMEDIA_TYPE_SUBTITLE,
    .p.id           = AV_CODEC_ID_DVB_TELETEXT,
    .priv_data_size = sizeof(DvbTxtContext),
    .init           = dvbtxt_encode_init,
    FF_CODEC_ENCODE_SUB_CB(dvbtxt_encode_frame),
    .close          = dvbtxt_encode_close,
};
